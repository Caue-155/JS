let frutas = ["maçã", "jaca", "laranja"]
console.log(frutas)

// Adcionando um elemento ao final do array
frutas.push("manga")
console.log(frutas)

// Removendo o último elemento do array
frutas.pop()
console.log(frutas)

// Adcionando um elemento ao início do array
frutas.unshift("uva")
console.log(frutas)

//Removendo o primeiro elemento do array
frutas.shift()
console.log(frutas)